public class Student {
    private String matrNr;
    private String name;

    public Student(String matrNr, String name) {
        this.matrNr = matrNr;
        this.name = name;
    }

    public String getMatrNr() {
        return matrNr;
    }

    public String getName() {
        return name;
    }
}
